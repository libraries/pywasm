def read_u32_leb128(r: typing.BinaryIO):
    v = 0
    n = 0
    for _ in range(0, 9):
        b = ord(r.read(1))
        tmp = b & 0x7f
        v = tmp << (n * 7) | v
        if (b & 0x80) != 0x80:
            break
        n += 1
    if n == 4 and (tmp & 0xf0) != 0:
        return -1
    return n + 1, v


def read_u64_leb128(r: typing.BinaryIO):
    v = 0
    n = 0
    for _ in range(0, 9):
        b = ord(r.read(1))
        tmp = b & 0x7f
        v = tmp << (n * 7) | v
        if (b & 0x80) != 0x80:
            break
        n += 1
    if n == 8 and (tmp & 0xf0) != 0:
        return -1
    return n + 1, v


def rotl_u32(x: int, k: int):
    x = into_u32(x)
    k = into_u32(k)
    n = 32
    s = k & (n - 1)
    return x << s | x >> (n - s)


def rotl_u64(x: int, k: int):
    x = into_u64(x)
    k = into_u64(k)
    n = 64
    s = k & (n - 1)
    return x << s | x >> (n - s)


def rotr_u32(x: int, k: int):
    return rotl_u32(x, -k)


def rotr_u64(x: int, k: int):
    return rotl_u64(x, -k)
