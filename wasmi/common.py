def into_i32(n: int):
    if n > I32_MAX:
        return into_i32(n - I32_LEN)
    if n < I32_MIN:
        return into_i32(n + I32_LEN)
    return n


def into_i64(n: int):
    if n > I64_MAX:
        return into_i64(n - I64_LEN)
    if n < I64_MIN:
        return into_i64(n + I64_LEN)
    return n


def into_u32(n: int):
    if n > U32_MAX:
        return into_u32(n - U32_MAX)
    if n < 0:
        return into_u32(n + U32_MAX)
